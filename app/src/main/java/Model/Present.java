package Model;

public class Present {
    private String ten;
    private String mota;


    public Present(String ten,String mota) {
        this.ten = ten;
        this.mota=mota;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getMota() {
        return mota;
    }

    public void setMota(String mota) {
        this.mota = mota;
    }


}
